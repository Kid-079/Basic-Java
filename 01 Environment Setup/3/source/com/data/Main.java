// Keywords
package com.data;

public class Main {
	public static void dataPublic() {
		System.out.println("Good Afternoon : Public");
	}

	public static void dataPrivate() {
		System.out.println("Good Afternoon : Private");
	}

	static void dataDefault() {
		System.out.println("Good Afternoon : Default");
	}

	protected static void dataProtected() {
		System.out.println("Good Afternoon : Protected");
	}
}

