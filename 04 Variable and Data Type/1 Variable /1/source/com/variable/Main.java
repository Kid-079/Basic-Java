package com.variable;

public class Main {
	public static void main(String[] args){
		// Create Variable
		// Data Type
		int a = 10; // assignment
		System.out.println("Value a = " + a);

		// Change Value
		a = 20;
		System.out.println("Value new_a = " + a);

		// Create Declaration
		int b;    // Declaration
		b = 40;   // Insert Value

		System.out.println("Value b = " + b);

	}
}